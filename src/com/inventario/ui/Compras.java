    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
      int row = table.getSelectedRow();
        int column = table.getColumnCount();
        Object[] val = new Object[column];
        for(int i = 0; i < column; i++) {
            val[i]=table.getValueAt(row, i);
        }
        purchaseIdTxt.setText(val[0].toString());
        String sup=new ProductoDAO().getProductsSupplier(Integer.parseInt(purchaseIdTxt.getText()));
        supplierInfoLab.setText("Comprado a "+sup);
        String purchasedDate=new ProductoDAO().getPurchasedDate(Integer.parseInt(purchaseIdTxt.getText()));
        purchasedDateLab.setText(purchasedDate);
        
        quantity=Integer.parseInt(val[3].toString());
        productCode=val[1].toString();
    }//GEN-LAST:event_tableMouseClicked

